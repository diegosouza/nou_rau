defmodule NouRau.Collections do
  @moduledoc """
  The Collections context.
  """

  import Ecto.Query, warn: false
  alias NouRau.Repo

  alias NouRau.Collections.Document

  @doc """
  Returns the list of documents.

  ## Examples

      iex> list_documents()
      [%Document{}, ...]

  """
  def list_documents do
    Repo.all from Document, preload: :category
  end

  @doc """
  Gets a single document.

  Raises `Ecto.NoResultsError` if the Document does not exist.

  ## Examples

      iex> get_document!(123)
      %Document{}

      iex> get_document!(456)
      ** (Ecto.NoResultsError)

  """
  def get_document!(id), do: Repo.get!(Document, id) |> Repo.preload([:category, :attachment])

  @doc """
  Creates a document.

  ## Examples

      iex> create_document(%{field: value})
      {:ok, %Document{}}

      iex> create_document(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_document(attrs \\ %{}) do
    %Document{}
    |> Document.changeset(attrs)
    |> Repo.insert()
  end

  def create_document_from(document, attrs \\ %{}, callback \\ &{:ok, &1}) do
    document
    |> Document.changeset(attrs)
    |> Repo.insert()
    |> after_save(callback)
  end

  defp after_save({:ok, document}, callback) do
    {:ok, _} = callback.(document)
  end

  defp after_save(error, _callback), do: error

  @doc """
  Updates a document.

  ## Examples

      iex> update_document(document, %{field: new_value})
      {:ok, %Document{}}

      iex> update_document(document, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_document(%Document{} = document, attrs, callback \\ &{:ok, &1}) do
    document
    |> Document.changeset(attrs)
    |> Repo.update()
    |> after_save(callback)
  end

  @doc """
  Deletes a document.

  ## Examples

      iex> delete_document(document)
      {:ok, %Document{}}

      iex> delete_document(document)
      {:error, %Ecto.Changeset{}}

  """
  def delete_document(%Document{} = document) do
    Repo.delete(document)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking document changes.

  ## Examples

      iex> change_document(document)
      %Ecto.Changeset{data: %Document{}}

  """
  def change_document(%Document{} = document, attrs \\ %{}) do
    Document.changeset(document, attrs)
  end

  alias NouRau.Collections.Category

  def root_categories, do: Category.roots() |> Repo.all()

  @doc """
  Returns the list of categories.

  ## Examples

      iex> list_categories()
      [%Category{}, ...]

  """
  def list_categories do
    Repo.all(Category)
  end

  def list_categories_for_another() do
    all = Repo.all from(Category), preload: [:parent]
    [ %Category{ id: "", name: "Without parent category (root)" } | all ]
    |> Enum.reject(& &1.name == "Uncategorized" )
  end

  def list_categories_with_subcategories_count do
    # TODO: get categories with subcategories_count set
    from(Category)
    |> Repo.all()
  end

